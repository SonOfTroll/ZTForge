"""
OPA integration via HTTP.

Communicates with OPA's REST API to evaluate policies and push
canvas-derived rules. OPA runs as a sidecar container.
"""

import httpx
from typing import Any

from app.core.config import get_settings
from app.core.logging import get_logger

logger = get_logger("ztforge.policy_engine")


class PolicyEngine:
    def __init__(self, opa_url: str | None = None):
        self.opa_url = opa_url or get_settings().opa_url

    async def evaluate(self, policy_path: str, input_data: dict[str, Any]) -> dict[str, Any]:
        """Query OPA for a policy decision."""
        url = f"{self.opa_url}/v1/data/{policy_path}"
        try:
            async with httpx.AsyncClient(timeout=5.0) as client:
                resp = await client.post(url, json={"input": input_data})
                resp.raise_for_status()
                return resp.json().get("result", {})
        except httpx.ConnectError:
            logger.warning("opa_unreachable", url=url)
            # Fail closed — deny by default when OPA is unreachable
            return {"allow": False, "reason": "Policy engine unreachable — defaulting to deny"}
        except httpx.HTTPStatusError as e:
            logger.error("opa_error", status=e.response.status_code, detail=str(e))
            return {"allow": False, "reason": f"Policy engine error: {e.response.status_code}"}

    async def push_policy(self, policy_id: str, rego_content: str) -> bool:
        """Push a Rego policy to OPA."""
        url = f"{self.opa_url}/v1/policies/{policy_id}"
        try:
            async with httpx.AsyncClient(timeout=5.0) as client:
                resp = await client.put(
                    url,
                    content=rego_content,
                    headers={"Content-Type": "text/plain"},
                )
                resp.raise_for_status()
                logger.info("policy_pushed", policy_id=policy_id)
                return True
        except (httpx.ConnectError, httpx.HTTPStatusError) as e:
            logger.error("policy_push_failed", policy_id=policy_id, error=str(e))
            return False

    async def push_data(self, data_path: str, data: dict[str, Any]) -> bool:
        """Push data to OPA's data store."""
        url = f"{self.opa_url}/v1/data/{data_path}"
        try:
            async with httpx.AsyncClient(timeout=5.0) as client:
                resp = await client.put(url, json=data)
                resp.raise_for_status()
                return True
        except (httpx.ConnectError, httpx.HTTPStatusError) as e:
            logger.error("data_push_failed", path=data_path, error=str(e))
            return False

    async def health_check(self) -> bool:
        try:
            async with httpx.AsyncClient(timeout=3.0) as client:
                resp = await client.get(f"{self.opa_url}/health")
                return resp.status_code == 200
        except httpx.ConnectError:
            return False


